void	ft_print_comb2(void)
{
	int n;
	int d;

	n = 0;
	while (n <= 9899)
	{
		d = (n / 10) % 10;
		if (n / 1000 < d || (n / 1000 == d && (n / 100) % 10 < n % 10))
		{
			ft_putchar('0' + n / 1000);
			ft_putchar('0' + (n / 100) % 10);
			ft_putchar(' ');
			ft_putchar('0' + d);
			ft_putchar('0' + n % 10);
			if ('0' + n / 1000 == '9' && '0' + (n / 100) % 10 == '8'
				&& '0' + d == '9' && '0' + n % 10 == '9')
				break ;
			ft_putchar(',');
			ft_putchar(' ');
		}
		n++;
	}
}
