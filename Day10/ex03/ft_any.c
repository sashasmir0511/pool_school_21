int		ft_any(char **tab, int (*f)(char *))
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (tab[i] != 0)
	{
		j = 0;
		while (tab[j] != 0)
		{
			if (f(&tab[i][j]) == 1)
				return (1);
			j++;
		}
	}
	return (0);
}
