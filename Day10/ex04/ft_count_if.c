int		ft_count_if(char **tab, int (*f)(char *))
{
	int i;
	int j;
	int num;

	i = 0;
	num = 0;
	while (tab[i] != 0)
	{
		j = 0;
		while (tab[i][j] != 0)
		{
			if (f(&tab[i][j]) == 1)
				num++;
			j++;
		}
		i++;
	}
	return (num);
}
