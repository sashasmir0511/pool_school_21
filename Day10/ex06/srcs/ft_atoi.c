#include "../includes/ft_put.h"

int	ft_atoi(char *str)
{
	int i;
	int res;
	int n;

	i = 0;
	n = 1;
	res = 0;
	while (str[i] >= 0 && str[i] <= 32)
		i++;
	if (str[i] == '-' && str[i + 1] >= '0' && str[i + 1] <= '9')
	{
		n = -1;
		i++;
	}
	if (str[i] == '+' && str[i + 1] >= '0' && str[i + 1] <= '9')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = 10 * res + (str[i] - '0');
		i++;
	}
	res = res * n;
	return (res);
}
