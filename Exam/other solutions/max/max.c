int		max(int *tab, unsigned int len)
{
	unsigned int	i;
	int				mx;

	if (len <= 0)
		return (0);
	i = 0;
	mx = tab[i];
	while (i < len)
	{
		if (tab[i] > mx)
			mx = tab[i];
		i++;
	}
	return (mx);
}
